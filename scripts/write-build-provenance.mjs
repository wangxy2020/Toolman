#!/usr/bin/env node
/**
 * Bake build-time provenance / code fingerprint for Toolman desktop builds.
 * Output: packages/shared/src/provenance/build-provenance.generated.ts
 */
import { createHash } from 'node:crypto'
import { execSync } from 'node:child_process'
import { readFileSync, writeFileSync, mkdirSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..')
const OUT_FILE = join(ROOT, 'packages/shared/src/provenance/build-provenance.generated.ts')
const DESKTOP_PKG = join(ROOT, 'apps/desktop/package.json')

function runGit(args, fallback = '') {
  try {
    return execSync(`git ${args}`, { cwd: ROOT, encoding: 'utf8' }).trim()
  } catch {
    return fallback
  }
}

function readVersion() {
  try {
    const pkg = JSON.parse(readFileSync(DESKTOP_PKG, 'utf8'))
    return typeof pkg.version === 'string' ? pkg.version : '0.0.0'
  } catch {
    return '0.0.0'
  }
}

const version = readVersion()
const gitCommit = runGit('rev-parse HEAD', 'unknown')
const gitDirty = runGit('status --porcelain').length > 0
const builtAt = new Date().toISOString()
const copyrightNotice = 'Copyright (C) 2024–2026 Toolman Contributors'
const license = 'AGPL-3.0-or-later'
const repository = 'https://github.com/wangxy2020/Toolman'
const product = 'Toolman'

const fingerprintMaterial = [
  product,
  version,
  gitCommit,
  builtAt,
  license,
  repository,
  gitDirty ? 'dirty' : 'clean',
].join('|')

const buildFingerprint = createHash('sha256').update(fingerprintMaterial, 'utf8').digest('hex')
const buildId = buildFingerprint.slice(0, 16)

const payload = {
  product,
  version,
  copyrightNotice,
  license,
  repository,
  gitCommit,
  gitDirty,
  builtAt,
  buildId,
  buildFingerprint,
}

mkdirSync(dirname(OUT_FILE), { recursive: true })
writeFileSync(
  OUT_FILE,
  `/** AUTO-GENERATED by scripts/write-build-provenance.mjs — do not edit manually. */
export const TOOLMAN_BUILD_PROVENANCE = ${JSON.stringify(payload, null, 2)} as const
`,
  'utf8',
)

console.log(`Wrote build provenance ${buildId} (${gitCommit.slice(0, 7)}${gitDirty ? '-dirty' : ''})`)
